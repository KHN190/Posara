use std::io::Write;
use std::path::PathBuf;
use std::process::ExitCode;

use color_quant::NeuQuant;
use image::{GenericImageView, imageops::FilterType};

const FRAME_W: u32 = 480;
const FRAME_H: u32 = 320;
const DEFAULT_SPRITE: u32 = 64;
const SAMPLE_FACTOR: i32 = 10;

fn usage() -> ExitCode {
    eprintln!("usage: png2abe <input> [output.abe] [--size N | --full]");
    eprintln!("  default: fit into {0}x{0} (sprite). --full fits framebuffer {1}x{2}.", DEFAULT_SPRITE, FRAME_W, FRAME_H);
    eprintln!("  quantizes to 16 colors + emits pal() / sprite() cart.");
    eprintln!("  output to stdout if omitted.");
    ExitCode::from(2)
}

fn main() -> ExitCode {
    let raw: Vec<String> = std::env::args().skip(1).collect();
    let mut positional: Vec<String> = Vec::new();
    let mut max_side: u32 = DEFAULT_SPRITE;
    let mut iter = raw.into_iter();
    while let Some(a) = iter.next() {
        match a.as_str() {
            "--full"  => { max_side = FRAME_W.max(FRAME_H); }
            "--size"  => {
                let Some(v) = iter.next() else { return usage(); };
                match v.parse::<u32>() {
                    Ok(n) if n > 0 => max_side = n,
                    _ => { eprintln!("--size requires positive integer"); return ExitCode::from(2); }
                }
            }
            "-h" | "--help" => return usage(),
            _ => positional.push(a),
        }
    }
    let Some(inp) = positional.get(0) else { return usage(); };
    let inp = PathBuf::from(inp);
    let out_path = positional.get(1).map(PathBuf::from);

    let img = match image::open(&inp) {
        Ok(i) => i,
        Err(e) => { eprintln!("open {}: {e}", inp.display()); return ExitCode::from(1); }
    };
    let (sw, sh) = img.dimensions();
    let cap_w = FRAME_W.min(max_side);
    let cap_h = FRAME_H.min(max_side);
    let sx = (cap_w as f32 / sw as f32).min(1.0);
    let sy = (cap_h as f32 / sh as f32).min(1.0);
    let s = sx.min(sy);
    let img = if s < 1.0 {
        let nw = (sw as f32 * s).floor().max(1.0) as u32;
        let nh = (sh as f32 * s).floor().max(1.0) as u32;
        eprintln!("resizing {}x{} → {}x{} ({} px)", sw, sh, nw, nh, nw * nh);
        img.resize(nw, nh, FilterType::Lanczos3)
    } else { img };
    let (w, h) = img.dimensions();
    let rgba = img.to_rgba8();
    let n_px = (w * h) as usize;

    let nq = NeuQuant::new(SAMPLE_FACTOR, 16, rgba.as_raw());
    let pal_rgba = nq.color_map_rgba();
    let mut palette_888 = [0u32; 16];
    for i in 0..16 {
        let r = pal_rgba[i * 4]     as u32;
        let g = pal_rgba[i * 4 + 1] as u32;
        let b = pal_rgba[i * 4 + 2] as u32;
        palette_888[i] = (r << 16) | (g << 8) | b;
    }
    let mut indices = Vec::with_capacity(n_px);
    for i in 0..n_px {
        let p = &rgba.as_raw()[i * 4..i * 4 + 4];
        indices.push(nq.index_of(p) as u8 & 0xF);
    }

    let mut cells = Vec::with_capacity((n_px + 7) / 8);
    let mut i = 0;
    while i < n_px {
        let mut v: u64 = 0;
        for k in 0..8 {
            let idx = if i + k < n_px { indices[i + k] as u64 } else { 0 };
            let shift = (7 - k) * 4;
            v |= idx << shift;
        }
        cells.push(v as i64);
        i += 8;
    }

    let mut out: Box<dyn Write> = match out_path.as_ref() {
        Some(p) => match std::fs::File::create(p) {
            Ok(f) => Box::new(f),
            Err(e) => { eprintln!("create {}: {e}", p.display()); return ExitCode::from(1); }
        },
        None => Box::new(std::io::stdout().lock()),
    };

    let center_x = (FRAME_W as i32 - w as i32) / 2;
    let center_y = (FRAME_H as i32 - h as i32) / 2;

    let _ = writeln!(out, "// generated by png2abe");
    let _ = writeln!(out, "// {}x{} palette-indexed (16 colors), {} cells", w, h, cells.len());
    let _ = writeln!(out);
    let _ = writeln!(out, "fn main() -> Unit {{ () }}");
    let _ = writeln!(out);
    let _ = writeln!(out, "pub fn start() -> Unit {{");
    let _ = writeln!(out, "  let cfg = 480 + 320 * 65536 + 1 * 4294967296;");
    let _ = writeln!(out, "  device_in(0x2000, cfg);");
    let _ = writeln!(out, "  cls(0x0000);");
    for (i, &c) in palette_888.iter().enumerate() {
        let _ = writeln!(out, "  pal({:2}, 0x{:06x});", i, c);
    }
    let row_cells = ((w as usize) + 7) / 8;
    let _ = write!(out, "  sprite([");
    for (i, &cell) in cells.iter().enumerate() {
        if i > 0 { let _ = write!(out, ","); }
        if i % row_cells == 0 { let _ = write!(out, "\n    "); }
        let _ = write!(out, "0x{:08x}", cell as u32);
    }
    let _ = writeln!(out, "\n  ], {}, {}, {}, {})", center_x.max(0), center_y.max(0), w, h);
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "pub fn update() -> Unit {{");
    let _ = writeln!(out, "  device_in(0x2001, 1)");
    let _ = writeln!(out, "}}");

    ExitCode::SUCCESS
}
